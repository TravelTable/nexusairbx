/** ---------- Helpers ---------- */
function uid(prefix = "id") {
  return `${prefix}_${Math.random().toString(16).slice(2)}_${Date.now().toString(16)}`;
}

function normalizeRbxAssetId(input) {
  const raw = String(input || "").trim();
  if (!raw) return "";
  if (raw.startsWith("rbxassetid://")) return raw;
  // if user pastes just digits
  if (/^\d+$/.test(raw)) return `rbxassetid://${raw}`;
  return raw; // allow other valid formats if they exist
}

function isImageIdLike(v) {
  const s = String(v || "").trim();
  return /^rbxassetid:\/\/\d+$/.test(s) || /^\d+$/.test(s);
}

