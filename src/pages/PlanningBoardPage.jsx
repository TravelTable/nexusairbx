function validateItem(item) {
  const blocking = [];
  const clarifications = [];

  // Required asset rule: ImageButton/ImageLabel must have at least 1 valid ImageId
  if (item.type === "ImageButton" || item.type === "ImageLabel") {
    const ids = (item.imageIds || []).map((x) => String(x?.id || "").trim()).filter(Boolean);
    const hasValid = ids.some((v) => isImageIdLike(v) || /^rbxassetid:\/\/\d+$/.test(v));
    if (!hasValid) {
      blocking.push({
        itemId: item.id,
        field: "imageIds",
        reason: `${item.type} requires an ImageId (rbxassetid://123...).`,
      });
    }
  }

  // Modal sanity: must have a close path
  if ((item.tags || []).includes("modal")) {
    const hasClose = (item.behaviors || []).some(
      (b) =>
        (b.trigger === "OnClick" && (b.action === "CloseUI" || b.action === "ToggleUI")) ||
        b.action === "CloseUI"
    );
    if (!hasClose) {
      clarifications.push({
        itemId: item.id,
        question:
          "This looks like a modal. Should it close when clicking outside, pressing ESC, or via a close button?",
      });
    }
  }

  // FireEvent needs event type clarification
  const hasFireEvent = (item.behaviors || []).some((b) => b.action === "FireEvent");
  if (hasFireEvent) {
    clarifications.push({
      itemId: item.id,
      question:
        "You used FireEvent. Should this be a RemoteEvent (client->server) or BindableEvent (local-only)?",
    });
  }

  return { blocking, clarifications };
}

